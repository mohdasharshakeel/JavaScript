const num = [1, 2, 3, 4, 5];

const getElem = (arr, target) => {
    console.log(`the element at index ${target} is ${arr[target]}`);

}

getElem(num, 3);


//! this is the exmaple of O(1) complexity this is constant time complexity because no matter how big the array is it will always take the same time to get the element at the target index.